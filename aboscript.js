function normalize(str) {
    // Preserve Devanagari characters; only clean ASCII punctuation
    return str.toLowerCase()
              .replace(/[^\u0900-\u097Fa-z0-9\s+\-]/gi, ' ')
              .replace(/\s+/g, ' ')
              .trim();
}
 
// Score a single entry against the normalized input
function scoreEntry(entry, input) {
    const words = input.split(/\s+/).filter(w => w.length > 1);
    let score = 0;
 
    // Full phrase matching (highest weight)
    for (const tag of entry.tags) {
        if (input.includes(tag)) {
            score += tag.split(' ').length * 4;
        }
    }
 
    // Word-level matching
    for (const word of words) {
        for (const tag of entry.tags) {
            if (tag === word || tag.includes(word) || word.includes(tag)) {
                score += 1;
            }
        }
    }
    return score;
}
 
// Split a message into potential sub-segments (greeting part + question part)
function splitMultiTopic(input) {
    // Common greeting words that might appear before a blood question
    const greetingWords = [
        "hi","hello","hey","namaste","namaskar","good morning","good afternoon",
        "good evening","good night","howdy","sup","yo","hii","नमस्ते","नमस्कार"
    ];
    const lower = input.toLowerCase();
 
    // Check if input starts with a greeting followed by more content
    let greetingPart = null;
    let remainderPart = input;
 
    for (const gw of greetingWords) {
        if (lower.startsWith(gw) && lower.length > gw.length + 2) {
            // There's content after the greeting
            greetingPart = input.substring(0, gw.length).trim();
            remainderPart = input.substring(gw.length).replace(/^[,\s]+/, '').trim();
            break;
        }
    }
 
    // Also check for comma/space-separated parts like "hello, tell me about blood"
    if (!greetingPart) {
        const separators = [',', ' and ', ' - ', '. '];
        for (const sep of separators) {
            const idx = lower.indexOf(sep);
            if (idx > 0 && idx < 20) { // greeting is usually short
                const firstPart = input.substring(0, idx).trim();
                const secondPart = input.substring(idx + sep.length).trim();
                // Check if first part is a greeting
                const firstNorm = normalize(firstPart);
                for (const gw of greetingWords) {
                    if (firstNorm === gw || firstNorm.includes(gw)) {
                        greetingPart = firstPart;
                        remainderPart = secondPart;
                        break;
                    }
                }
                if (greetingPart) break;
            }
        }
    }
 
    return { greetingPart, remainderPart };
}
 
function findAnswer(userInput) {
    const rawInput = userInput.trim();
    const input = normalize(rawInput);
 
    // ── LANGUAGE DETECTION ──
    const isNepali = detectNepali(rawInput);
    if (isNepali) setLanguage('ne');
 
    // ── MULTI-TOPIC DETECTION ──
    // Check if message has a greeting + blood question together
    const { greetingPart, remainderPart } = splitMultiTopic(input);
 
    if (greetingPart && remainderPart && remainderPart.length > 3) {
        // Find greeting response
        const greetInput = normalize(greetingPart);
        let greetEntry = null, greetScore = 0;
        for (const entry of knowledgeBase) {
            const s = scoreEntry(entry, greetInput);
            if (s > greetScore) { greetScore = s; greetEntry = entry; }
        }
 
        // Find topic response
        let topicEntry = null, topicScore = 0;
        const topicInput = normalize(remainderPart);
        for (const entry of knowledgeBase) {
            // Skip greeting-type entries for the second part
            if (entry.type === 'greeting' || entry.type === 'nepali_greeting') continue;
            const s = scoreEntry(entry, topicInput);
            if (s > topicScore) { topicScore = s; topicEntry = entry; }
        }
 
        // Combine both if found
        if (greetScore >= 1 && topicScore >= 1 && greetEntry && topicEntry) {
            const greetResponse = typeof greetEntry.respond === 'function'
                ? greetEntry.respond()
                : greetEntry.answer;
            const topicResponse = typeof topicEntry.respond === 'function'
                ? topicEntry.respond()
                : topicEntry.answer;
            return greetResponse + "\n\n" + "─".repeat(40) + "\n\n" + topicResponse;
        }
    }
 
    // ── STANDARD SINGLE-TOPIC MATCHING ──
    let bestScore = 0;
    let bestEntry = null;
 
    for (const entry of knowledgeBase) {
        const score = scoreEntry(entry, input);
        if (score > bestScore) {
            bestScore = score;
            bestEntry = entry;
        }
    }
 
    if (bestScore >= 1 && bestEntry) {
        if (typeof bestEntry.respond === 'function') {
            return bestEntry.respond();
        }
        return bestEntry.answer;
    }
 
    // ── FALLBACK ──
    if (currentLanguage === 'ne') {
        return "माफ गर्नुहोस्, मैले त्यो प्रश्न बुझिनँ। 🩸\n\nकृपया यी विषयमा सोध्नुहोस्:\n• 'रगत समूह के हो?'\n• 'O negative कसलाई दिन सकिन्छ?'\n• 'रक्तदान गर्न म योग्य छु?'\n• 'सबैभन्दा दुर्लभ रगत समूह कुन हो?'\n\nवा नजिकको रक्तदान केन्द्रमा सम्पर्क गर्नुहोस्!";
    }
    return "I couldn't find a specific answer to that in my database. 🩸\n\nTry asking about:\n• 'What are the blood types?'\n• 'Who can O negative donate to?'\n• 'Am I eligible to donate?'\n• 'What is the rarest blood type?'\n• 'Namaste' (to switch to Nepali)\n\nOr visit your nearest blood bank for personalized guidance!";
}
 
 
